public class class910_Lab3_2 {
    public static void main(String[] args) {

        // 半径
        final double radius = 5.0;

        // 円周率
        final double pI = 3.14159;

        // 計算式
        double area = pI * (radius * radius);

        // 出力
        System.out.printf("The area of the circle is: %.2f\n ", area);
    }
}
